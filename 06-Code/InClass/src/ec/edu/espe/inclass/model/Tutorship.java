public class Tutorship {

    private Date date;

    public Tutorship() {
        date = new Date();
    }

    public Tutorship(Date date) {
        this.date = date;
    }

    /**
     * @return the date
     */
    public Date getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(Date date) {
        this.date = date;
    }

}
